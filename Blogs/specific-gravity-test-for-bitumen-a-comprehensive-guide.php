                <h2 class="section-title">Understanding the Specific Gravity Test Procedure</h2>
                <p>
                    The specific gravity of bitumen is determined using a pycnometer. The process involves weighing the empty pycnometer, filling it with a known volume of bitumen, and weighing it again. The specific gravity is then calculated by dividing the bitumen weight by the weight of an equal volume of water.
                </p>
                <p>
                    Limitations include a lack of chemical composition data, susceptibility to temperature variations, and possible contamination. This test should be supplemented with other assessments for a complete analysis of bitumen properties.
                </p>
