                    <li><strong>Temperature:</strong> Higher temperatures reduce bitumen density and specific gravity, while lower temperatures increase both.</li>
                    <li><strong>Composition:</strong> The content of hydrocarbons, asphaltenes, and aromatics in bitumen also affects specific gravity. Higher asphaltene content results in a higher specific gravity, while higher aromatic content lowers it.</li>
